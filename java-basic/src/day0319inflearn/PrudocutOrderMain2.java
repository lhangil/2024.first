package day0319inflearn;

public class PrudocutOrderMain2 {

	public static void main(String[] args) {
		ProductOrder[] orders= new ProductOrder[3];
		orders[0]= createOrder("두부", 2000,2);
		orders[1]= createOrder("김치", 5000,1);
		orders[2]= createOrder("콜라", 1500,2);
		
		printOrders(orders); //static void printOrders 호출
		int totalAmount=getTotalAmount(orders);
		System.out.println("총 결제 금액:" + totalAmount);
		
	}
	
	static ProductOrder createOrder(String productName, int price, int quantity) { // 이건왜 해주는거?

		ProductOrder order = new ProductOrder(); // 이건 왜해주는거?
		order.productName = productName;
		order.price = price;
		order.quantity = quantity;
		return order;
	}	
	
	static void printOrders(ProductOrder[] orders) {
		for (ProductOrder order : orders) {
			System.out.println("상품명:" + order. productName + 
					",가격:"+ order.price +",수량:"+order.quantity);}
		
		}
	
	
	static int getTotalAmount(ProductOrder[] orders) {
		int totalAmount = 0;
		for (ProductOrder order : orders) {
			totalAmount += order.price *order.quantity;
		}
		return totalAmount;
		}
	}

